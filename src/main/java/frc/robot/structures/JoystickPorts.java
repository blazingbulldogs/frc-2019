package frc.robot.structures;

/**
 * Human readable variables for every numerical port on a joystick.
 */
public final class JoystickPorts {
  // Lettered buttons
  public static final int aButton = 0;
  public static final int bButton = 1;
  public static final int xButton = 2;
  public static final int yButton = 3;

  // Triggers and trigger buttons
  public static final int leftBumper = 4;
  public static final int rightBumper = 5;
  public static final int leftTrigger = 6;
  public static final int rightTrigger = 7;

  // Special buttons
  public static final int backButton = 8;
  public static final int startButton = 9;

  // Joystick presses
  public static final int leftStick = 10;
  public static final int rightStick = 11;

  // Directional pad
  public static final int dpadUp = 12;
  public static final int dpadDown = 13;
  public static final int dpadLeft = 14;
  public static final int dpadRight = 15; 

  // Joystick axes
  public static final int leftXAxis = 0;
  public static final int leftYAxis = 1;
  public static final int leftTriggerAxis = 2;
  public static final int rightTriggerAxis = 3;  
  public static final int rightXAxis = 4;
  public static final int rightYAxis = 5;
}